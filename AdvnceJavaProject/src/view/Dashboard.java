    public void showTotalStudentCount(){
         int totalStudentCount = 0; // Initialize total count to 0

    String query = "SELECT COUNT(*) AS roll FROM studentdata"; // Get the total count of students
    DbCon dbCon = new DbCon();

    try (Connection connection = dbCon.getCon(); PreparedStatement ps = connection.prepareStatement(query);
         ResultSet rs = ps.executeQuery()) {

        if (rs.next()) {
            totalStudentCount = rs.getInt("roll"); // Get the total student count
        }

        // Set the total student count to a label or display it as needed
        totalStudent.setText("     " + totalStudentCount);

    } catch (SQLException ex) {
        Logger.getLogger(NewAdmissionTest.class.getName()).log(Level.SEVERE, null, ex);
    }
    }
    
    
    
    
    
    
