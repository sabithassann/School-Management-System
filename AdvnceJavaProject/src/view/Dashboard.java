    public void showAll(){
        int[] classCounts = new int[12];

    String query = "SELECT stdClass, COUNT(*) AS roll FROM studentdata GROUP BY stdClass";
    DbCon dbCon = new DbCon();

    try (Connection connection = dbCon.getCon(); PreparedStatement ps = connection.prepareStatement(query);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            int classNumber = rs.getInt("stdClass");
            int count = rs.getInt("roll");
            classCounts[classNumber - 1] = count; // Subtract 1 to use as array index
        }

        // Set the counts to labels
        showClassOne.setText("" + classCounts[0]);
        showClassTwo.setText("" + classCounts[1]);
        showClassThree.setText("" + classCounts[2]);
        showClassFour.setText("" + classCounts[3]);
        showClassFive.setText("" + classCounts[4]);
        showClassSix.setText("" + classCounts[5]);
        showClassSeven.setText("" + classCounts[6]);
        showClassEight.setText("" + classCounts[7]);
        showClassNine.setText("" + classCounts[8]);
        showClassTen.setText("" + classCounts[9]);
        showClassElevne.setText("" + classCounts[10]);
        showClassTwelve.setText("" + classCounts[11]);
        // Similarly, set counts for other classes

    } catch (SQLException ex) {
        Logger.getLogger(NewAdmissionTest.class.getName()).log(Level.SEVERE, null, ex);
    }
    }
    
