    public StudentData retrieveStudentData(int roll, int stdClass){
        StudentData studentData1=null;
        
        DbCon databaseConnection=new DbCon();
        Connection con=databaseConnection.getCon();
        
        if(con !=null){
            
            try {
                String query="select * from studentdata where roll=? and stdClass=?";
                PreparedStatement pstmt=con.prepareStatement(query);
                
                pstmt.setInt(1, roll);
                pstmt.setInt(2, stdClass);
                ResultSet rs=pstmt.executeQuery();
                
                if(rs.next()){
                    String name=rs.getString("name");
                    String gender=rs.getString("gender");
                    String address=rs.getString("address");
                    Date dob=rs.getDate("dob");
                    int age=rs.getInt("age");
                    String picturePath=rs.getString("picturePath");
                    
                    //studentData1 =new StudentData(traineeId, batchId, name, gender, address, dob, age, gender, dob, batchId, name, picturePath);
                    studentData1=new StudentData(roll, stdClass, name, name, name, name, address, gender, dob, age, picturePath, gender, ABORT, dob, name, picturePath);
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Profiles.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        return studentData1;
        
    }

