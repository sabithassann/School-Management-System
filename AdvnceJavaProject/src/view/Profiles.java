    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        // TODO add your handling code here:
        
        if(txtRoll.getText().isEmpty() || txtClass.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Field cannot Be Empty");
            return;
        }
       
        
        
        int roll=Integer.parseInt(txtRoll.getText().trim());
        int stdClass=Integer.parseInt(txtClass.getText().trim());
        
        
        StudentData studentData=retrieveStudentData(roll, stdClass);
        
        if(studentData !=null){
            
            profileName.setText("Name : "+studentData.getName());
            profileAddress.setText("Address: "+ studentData.getAddress());
            profileGender.setText("Gender: "+studentData.getGender());
            profileDob.setText("DOB: "+studentData.getDob().toString());
            profileAge.setText("Age: "+studentData.getAge());
            profileTraineeId.setText("Roll : "+studentData.getRoll());
            profileBatchId.setText("Class : "+studentData.getStdClass());
            
            
            ImageIcon imageIcon=new ImageIcon(studentData.getPicturePath());
            Image image=imageIcon.getImage().getScaledInstance(profilePic.getWidth(), profilePic.getHeight(), Image.SCALE_SMOOTH);
            profilePic.setIcon(new ImageIcon(image));
        }
        else{
            JOptionPane.showMessageDialog(this, "No student found for given Trainee Id and Batch Id");
            
        }
    }//GEN-LAST:event_btnShowActionPerformed

